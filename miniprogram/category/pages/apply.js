  async applyCategory({ detail }){
    try{
      const formData = detail.value
      if(!formData.icon) throw '请先上传一张logo图片'
      if(!formData.name) throw '您必须指定一个可用的名称'
      await app.$api.category.apply(formData)
      app.$toast('申请成功')
      wx.navigateBack({})
    }catch(error){
      console.log(error)
      app.$toast(error)
    }
  },

